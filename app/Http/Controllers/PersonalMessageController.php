     */
    public function update(Request $request, $id)
    {
        
        $personal_message=new PersonalMessage();

        $personal_message->from_id = user()->id;
        $personal_message->to_id = $id;
        $personal_message->message = $request->message;

        $personal_message->save();
        return back();
        
    }

